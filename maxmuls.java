import java.util.*;
public class maximumMultipleSum{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
 
        while(t-- >0){
           int n = sc.nextInt();
           
           if(n==3){
            System.out.print(3);
           }
           else{
            System.out.print(2);
           }
        }
    }
}